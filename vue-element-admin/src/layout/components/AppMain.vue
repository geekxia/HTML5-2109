<template>
  <section class="app-main">

    <transition name="fade-transform" mode="out-in">
      <!-- include指定哪些组件需要进行缓存 -->
      <!-- 在router-view显示组件，如果这个组件在cachedViews中存在，它将组件被缓存 -->
      <keep-alive :include="cachedViews">
        <router-view :key="key" />
      </keep-alive>

    </transition>

  </section>
</template>

<script>
export default {
  name: 'AppMain',
  computed: {
    cachedViews() {
      return this.$store.state.tagsView.cachedViews
    },
    key() {
      return this.$route.path
    }
  }
}
</script>

